function cellsAtPosition(x, y, cells) {
  let shiftedCells = [];
  for (let i = 0; i < cells.length; i++) {
    shiftedCells.push([cells[i][0] + x, cells[i][1] + y]);
  }
  return shiftedCells;
}

function numberOfColumns(shape) {
  return shape.indexOf("\n");
}
function numberOfRows(shape) {
  return (shape.lastIndexOf("\n") + 1) / (shape.indexOf("\n") + 1) + 1;
}
function shapeToCells(shape) {
  let cell = [];
  for (let i = 0; i < numberOfColumns(shape); i++) {
    for (let j = 0; j < numberOfRows(shape); j++) {
      if (shape.charAt(i + j * (numberOfColumns(shape) + 1)) === "O") {
        cell.push([i, j]);
      }
    }
  }
  return cell;
}

function mapShape(game,posX,posY,width,height,mapfunc) {
  for (let i = 0; i < width; i++) {
    for (let j = 0; j < height; j++) {
      game.gridToObj(posX+i,posY+j).alive=mapfunc(i,j);
    }
  }
}

function randomChoice(li) {
  const ind = Math.floor(Math.random() * li.length);
  return li[ind];
}

